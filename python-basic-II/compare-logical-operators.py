# comparison operators
# > is greater than
# < is less than
# == is equal to
# >= is greater or equal to
# <= is less than or equal to
# != is not equal to
# not() - returns the opposite boolean value of something
print(2 > 1)
print(1 < 2)
print(1 == 1)
print(1 >= 1)
print(2 >= 1)
print(1 <= 1)
print(1 <= 2)
print(1 != 2)
print(not(False))

